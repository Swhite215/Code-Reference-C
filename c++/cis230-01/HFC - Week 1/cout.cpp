#include <iostream>

int main () {

    std::cout<<"Programming is great fun!\n"; // << - stream insertion operatorw

    std::cout<<"Programming is "<<"great fun!\n";

    std::cout<<"Programming is ";
    std::cout<<"great fun!\n";

    std::cout<<"Programming is great fun!"<<std::endl;
    std::cout<<"Programming is great fun!\t TABBED"<<std::endl;
    std::cout<<"Programming is great fun!\a ALARMED"<<std::endl;
    std::cout<<"Programming is great fun!\b BACKED"<<std::endl;
    std::cout<<"Programming is great fun!\r RETURNED"<<std::endl;
    std::cout<<"Programming is great fun!\\ BACKSLASH"<<std::endl;
    std::cout<<"Programming is great fun!\' SINGLE QUOTE"<<std::endl;
    std::cout<<"Programming is great fun!\" DOUBLE QUOTE"<<std::endl;

    return 0;
}